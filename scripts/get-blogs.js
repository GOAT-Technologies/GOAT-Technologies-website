async function fetchBlogs() {
    try {
        const response = await fetch("/blog/all");
        const data = await response.json();
        const BlogListTop3 = document.getElementById("blog-list-top-3");
        if (data.status === "ok") {
            blogs = data.blogs;
            console.log({ blogs })
            const ResultingBlogTemplate = blogs.map(blog => BlogItemTemplate(blog)).join('');
            console.log(ResultingBlogTemplate);
            BlogListTop3.innerHTML = ResultingBlogTemplate;
        } else {
            BlogListTop3.innerHTML = DefaultBlogTemplate;
        }
    } catch (error) {
        console.error(error);
    }
};

